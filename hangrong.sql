CREATE DATABASE HANGRONG
GO
USE HANGRONG
GO

CREATE TABLE THANHVIEN
    (
      TENDANGNHAP NVARCHAR(50) PRIMARY KEY,
      MATKHAU NVARCHAR(100),
      TEN NVARCHAR(60) ,
      DIACHI NVARCHAR(60) ,
      SODIENTHOAI VARCHAR(30) ,
      NGAYTHAMGIA NVARCHAR(50) ,
      ANH NVARCHAR(100) ,
      CHUCNANG NVARCHAR(50)
    )
GO
CREATE TABLE MATHANG
    (
      MAMATHANG INT IDENTITY PRIMARY KEY,
      TENMATHANG NVARCHAR(100),
      NGAYDANG NVARCHAR(100) ,
      LoaiHang NVARCHAR(100),
      TINHTRANG NVARCHAR(50),
      Anh NVARCHAR(500),
      Gia NUMERIC(19,0),
      MOTA NVARCHAR(500),
      NGUOIBAN NVARCHAR(50) FOREIGN KEY REFERENCES THANHVIEN (TENDANGNHAP)
    )
GO
CREATE TABLE GIAODICH
(
	MaGiaoDich INT IDENTITY PRIMARY KEY,
	MAMATHANG INT  FOREIGN KEY REFERENCES MATHANG (MAMATHANG),
	TGDatHang NVARCHAR(50),
	TGNhanHang NVARCHAR(50),
	NguoiBan NVARCHAR(50) FOREIGN KEY REFERENCES THANHVIEN (TENDANGNHAP),
	NguoiMua NVARCHAR(50) FOREIGN KEY REFERENCES THANHVIEN (TENDANGNHAP)
)
GO
